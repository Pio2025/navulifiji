                    // Function to clear all stream settings
                    function clearAllStreamSettings() {
                        console.log('Clearing all stream settings...');
                        
                        // COMPLETELY REMOVE all stream settings
                        $('.stream-settings').remove();
                        
                        $('stream-separator').remove();
                        
                        // Uncheck all level checkboxes
                        $('input[name="levells[]"]').prop('checked', false);
                        
                        // Uncheck the "Select All" checkbox
                        $('#check_all_level').prop('checked', false);
                    }
                    
                    function updateLevelsVisibility() {
                        const selectedCategory = $('input[name="sch_category"]:checked').val();
                        const allowedLevels = categoryLevels[selectedCategory];
                        
                        $('input[name="levells[]"]').each(function() {
                            const levelValue = parseInt($(this).val());
                            const parentDiv = $(this).closest('.form-check');
                            
                            if (allowedLevels.includes(levelValue)) {
                                parentDiv.show();
                            } else {
                                parentDiv.hide();
                                $(this).prop('checked', false); // Uncheck hidden levels
                            }
                        });
                    }
                    
                    function toggleAllLevels() {
                        const isChecked = $('#check_all_level').prop('checked');
                        const selectedCategory = $('input[name="sch_category"]:checked').val();
                        const allowedLevels = categoryLevels[selectedCategory];
                        
                        if (isChecked) {
                            // Check all visible levels and show stream settings
                            $('input[name="levells[]"]').each(function() {
                                const levelValue = parseInt($(this).val());
                                if (allowedLevels.includes(levelValue)) {
                                    $(this).prop('checked', true);
                                    const levelName = levelNames[levelValue];
                                    updateStreamSettingsForLevel(levelValue, levelName, true);
                                }
                            });
                        } else {
                            // Uncheck all levels and REMOVE their stream settings completely
                            $('input[name="levells[]"]').each(function() {
                                const levelValue = parseInt($(this).val());
                                $(this).prop('checked', false);
                                
                                // COMPLETELY REMOVE stream settings for this level
                                $(`.stream-settings[data-level="${levelValue}"]`).remove();
                            });
                        }
                    }
                    
                    // Function to update stream settings for a specific level
                    function updateStreamSettingsForLevel(levelValue, levelName, isChecked) {
                        if (isChecked) {
                            // Check if stream settings already exist for this level
                            if ($(`.stream-settings[data-level="${levelValue}"]`).length === 0) {
                                // Create and append new stream settings
                                const streamSettingsHTML = createStreamSettings(levelName, levelValue);
                                streamSettingsContainer.append(streamSettingsHTML);
                            } else {
                                // Show existing stream settings
                                $(`.stream-settings[data-level="${levelValue}"]`).show();
                            }
                        } else {
                            // COMPLETELY REMOVE stream settings for this level
                            $(`.stream-settings[data-level="${levelValue}"]`).remove();
                        }
                    }
                    
                    // Handle individual level checkbox changes
                    $('input[name="levells[]"]').on('change', function() {
                        const levelValue = $(this).val();
                        const levelName = levelNames[levelValue];
                        const isChecked = $(this).prop('checked');
                        
                        if (isChecked) {
                            // Show stream settings when checked
                            updateStreamSettingsForLevel(levelValue, levelName, true);
                        } else {
                            // COMPLETELY REMOVE stream settings when unchecked
                            $(`.stream-settings[data-level="${levelValue}"]`).remove();
                        }
                    });
                    
                    // Handle "Check All" checkbox
                    $('#check_all_level').on('change', toggleAllLevels);
                    
