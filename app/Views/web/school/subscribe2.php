                            url: "<?= base_url('district/getDistrictByProvince') ?>",
                            type: 'POST',
                            data: {
                                id: id, 
                                '<?= csrf_token() ?>': '<?= csrf_hash() ?>'
                            },
                            dataType: 'json',
                            beforeSend: function(){
                                $("#loader").show();
                                $(".response").hide();
                            },
                            success: function(response){
